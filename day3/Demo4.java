package day3;

public class Demo4 {

	public static void main(String[] args) {
		
		// 0b 開頭表示二進制轉十進制
		int x = 0b00101;
		System.out.printf("x = %d%n", x);

		// 0 開頭表示八進制轉十進制
		int y = 017;
		System.out.printf("y = %d%n", y);

		// 0x 開頭表示十六進制轉十進制
		int z = 0xa4;
		System.out.printf("z = %d%n", z);
	}

}